package _02_nested_loops._3_for_loop_gauntlet;

public class ForLoopGauntlet {
    public static void main(String[] args) {
        for(int i = 0; i <= 100; i++) {
            System.out.println(i);
        }
        for(int i = 100; i >= 0; i--) {
            System.out.println(i);
        }
    }
}